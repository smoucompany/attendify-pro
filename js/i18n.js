// Active language
let currentLang = 'ar';

// Translate a key
function t(key, fallback) {
  const dict = TRANSLATIONS[currentLang] || TRANSLATIONS['ar'];
  return dict[key] || fallback || key;
}

// Apply translations to DOM
function applyTranslations() {
  const lang = currentLang;
  document.documentElement.lang = lang;
  document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';
  document.documentElement.dataset.lang = lang;

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    el.textContent = t(key);
  });

  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.dataset.i18nPlaceholder;
    el.placeholder = t(key);
  });

  // Update lang toggle button
  const langLabel = document.getElementById('lang-label');
  if (langLabel) langLabel.textContent = lang === 'ar' ? 'EN' : 'عربي';

  // Update lang buttons in login
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });
}

// Set language
function setLanguage(lang) {
  currentLang = lang;
  localStorage.setItem('app-lang', lang);
  applyTranslations();
}

// Init language from storage
function initLanguage() {
  const saved = localStorage.getItem('app-lang') || 'ar';
  currentLang = saved;
  // Apply direction immediately to <html> before any render
  document.documentElement.lang = saved;
  document.documentElement.dir  = saved === 'ar' ? 'rtl' : 'ltr';
  document.documentElement.dataset.lang = saved;
  applyTranslations();
}
