// Active language
let currentLang = 'ar';

// Translate a key
function t(key, fallback) {
  const dict = TRANSLATIONS[currentLang] || TRANSLATIONS['ar'];
  return dict[key] || fallback || key;
}

// Apply translations to DOM
function applyTranslations() {
  const lang = currentLang;
  const isRTL = lang === 'ar' || lang === 'ur';
  document.documentElement.lang = lang;
  document.documentElement.dir = isRTL ? 'rtl' : 'ltr';
  document.documentElement.dataset.lang = lang;

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    el.textContent = t(key);
  });

  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    el.placeholder = t(el.dataset.i18nPlaceholder);
  });

  // Update all lang selects
  document.querySelectorAll('.lang-select').forEach(sel => { sel.value = lang; });

  // Update header lang label (shows next lang name)
  const langLabel = document.getElementById('lang-label');
  if (langLabel) {
    const names = { ar:'AR', en:'EN', hi:'हि', ur:'UR', fil:'FIL' };
    langLabel.textContent = names[lang] || lang.toUpperCase();
  }

  // Auto-translate static sidebar elements (company name, user job title, etc.)
  if (lang !== 'ar' && typeof Translator !== 'undefined') {
    const sidebar = document.getElementById('sidebar');
    if (sidebar) Translator.translateEl(sidebar, lang);
  }
}

// Set language
function setLanguage(lang) {
  currentLang = lang;
  localStorage.setItem('app-lang', lang);
  applyTranslations();
}

// Init language from storage
function initLanguage() {
  const saved = localStorage.getItem('app-lang') || 'ar';
  currentLang = saved;
  // Apply direction immediately to <html> before any render
  document.documentElement.lang = saved;
  document.documentElement.dir  = saved === 'ar' ? 'rtl' : 'ltr';
  document.documentElement.dataset.lang = saved;
  applyTranslations();
}
